package day01;

public class integerExam {

	public static void main(String[] args) {
		int var1 = 0b1011; //2진수 리터럴
		int var2 = 0756; //8진수 리터럴
		int var3 = 0x32; // 16진수 리터럴
		int var4 = 235; //10진수 리터럴 
		
		System.out.println("var1은 : " + var1);
		System.out.println("var2은 : " + var2);
		System.out.println("var3은 : " + var3);
		System.out.println("var4은 : " + var4);
		
	}

}
